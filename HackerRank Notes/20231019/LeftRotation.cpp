vector<int> rotateLeft(int d, vector<int> a) {

    int n = a.size();
    vector<int> rotatedArray(n);

    // Calculate the effective number of rotations
    int rotations = d % n;

    // Copy the elements from original array to the rotated array
    for (int i = 0; i < n; ++i) {
        // Calculate the new index after rotation
        int newIndex = (i + rotations) % n;
        rotatedArray[i] = a[newIndex];
    }

    return rotatedArray;
}

